package com.example.consumer_client.farm;

public class FarmTotalInfo {
    private String farmProdImgView;
    private String farmName;
    private String farmMainItem;
    private String farmFeature;
    private int farmSituation;

    public String getFarmProdImgView() {
        return farmProdImgView;
    }

    public void setFarmProdImgView(String farmProdImgView) {
        this.farmProdImgView = farmProdImgView;
    }

    public String getFarmName() {
        return farmName;
    }

    public void setFarmName(String farmName) {
        this.farmName = farmName;
    }

    public String getFarmMainItem() {
        return farmMainItem;
    }

    public void setFarmMainItem(String farmMainItem) {
        this.farmMainItem = farmMainItem;
    }

    public String getFarmFeature() {
        return farmFeature;
    }

    public void setFarmFeature(String farmFeature) {
        this.farmFeature = farmFeature;
    }

    public int getFarmSituation() {
        return farmSituation;
    }

    public void setFarmSituation(int farmSituation) {
        this.farmSituation = farmSituation;
    }
}
